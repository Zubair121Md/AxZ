// Generate courses for both live and self-paced
// Live courses have higher prices and class timings
// Self-paced courses have lower prices and flexible timing
export const courses: Course[] = baseCourses.flatMap((base, index) => {
  const days = ["Mon, Wed, Fri", "Tue, Thu, Sat", "Mon, Wed", "Tue, Thu", "Sat, Sun"];
  const times = ["6:00 PM to 8:00 PM IST", "7:00 PM to 9:00 PM IST", "10:00 AM to 12:00 PM IST", "2:00 PM to 4:00 PM IST"];
  const dayTime = `${days[index % days.length]} - ${times[index % times.length]}`;
  const startDate = new Date();
  startDate.setDate(startDate.getDate() + (index % 30) + 7); // Start dates 7-37 days from now
  
  return [
    {
      ...base,
      id: `${index * 2 + 1}-live`,
      courseType: "live" as const,
      price: Math.round(base.price * 1.5), // 50% higher for live courses
      classTiming: dayTime,
      startDate: startDate.toISOString().split('T')[0],
    },
    {
      ...base,
      id: `${index * 2 + 2}-self-paced`,
      courseType: "self-paced" as const,
      price: base.price, // Original price for self-paced
    },
  ];
});

